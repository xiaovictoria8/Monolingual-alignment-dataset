class HIT_input(object):
    def __init__(self, pair_id, doc_id, segment_id, source, target, sure_align, poss_align, sourceHL, 
                 targetHL):
        self.pair_id = pair_id
        self.doc_id = doc_id
        self.segment_id = segment_id
        self.source = source
        self.target = target
        self.sure_align = sure_align
        self.poss_align = poss_align
        self.sourceHL = sourceHL
        self.targetHL = targetHL
    
class HIT_test_input(HIT_input):
    def __init__(self, pair_id, doc_id, segment_id, source, target, sure_align, poss_align, sourceHL, 
                 targetHL, ans_sure_align, ans_poss_align, ans_sourceHL, ans_targetHL):
        HIT_input.__init__(self, pair_id, doc_id, segment_id, source, target, sure_align, poss_align, 
                          sourceHL, targetHL)
        self.ans_sure_align = ans_sure_align
        self.ans_poss_align = ans_poss_align
        self.ans_sourceHL = ans_sourceHL
        self.ans_targetHL = ans_targetHL
        
class HIT_training_input(HIT_test_input):
    def __init__(self, pair_id, doc_id, segment_id, source, target, sure_align, poss_align, sourceHL, 
                     targetHL, ans_sure_align, ans_poss_align, ans_sourceHL, ans_targetHL, instructions, 
                     image):
        HIT_test_input.__init__(self, pair_id, doc_id, segment_id, source, target, sure_align, poss_align, 
                              sourceHL, targetHL, ans_sure_align, ans_poss_align, ans_sourceHL, 
                              ans_targetHL)
        self.instructions = instructions
        self.image = image

